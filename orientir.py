def question(message, q_index=0):
    if q_index < len(questions):
        markup = types.ReplyKeyboardMarkup(one_time_keyboard=True)
        itembtn1 = types.KeyboardButton(questions[q_index][0])
        itembtn2 = types.KeyboardButton(questions[q_index][1])
        markup.add(itembtn1, itembtn2)
        bot.send_message(message.chat.id, f"–í—ã–±–µ—Ä–∏—Ç–µ –æ–¥–∏–Ω –∏–∑ –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤:", reply_markup=markup)
        bot.register_next_step_handler(message, process_answer, q_index)
    else:
        finish_test(message)

def process_answer(message, q_index):
    # –ï—Å–ª–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –æ—Ç–ø—Ä–∞–≤–ª—è–µ—Ç –∫–æ–º–∞–Ω–¥—É /start, –Ω–∞—á–∏–Ω–∞–µ–º —Ç–µ—Å—Ç —Å–Ω–∞—á–∞–ª–∞
    if message.text == '/start':
        send_welcome(message)
        return

    # –ï—Å–ª–∏ –æ—Ç–≤–µ—Ç –Ω–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É–µ—Ç –∫–Ω–æ–ø–∫–∞–º, –ø—Ä–æ—Å–∏–º –ø–æ–≤—Ç–æ—Ä–∏—Ç—å –æ—Ç–≤–µ—Ç
    if message.text not in questions[q_index]:
        bot.send_message(message.chat.id, "–ü–æ–∂–∞–ª—É–π—Å—Ç–∞, –≤—ã–±–µ—Ä–∏—Ç–µ –æ–¥–∏–Ω –∏–∑ –ø—Ä–µ–¥–ª–æ–∂–µ–Ω–Ω—ã—Ö –≤–∞—Ä–∏–∞–Ω—Ç–æ–≤.")
        question(message, q_index)
        return


    user_answers[message.chat.id].append(message.text)
    question(message, q_index + 1)

